#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*s;
	size_t			i;

	s = (unsigned char *)b;
	i = 0;
	while (len > i)
	{
		*s++ = (unsigned char)c;
		i++;
	}
	return (b);
}
